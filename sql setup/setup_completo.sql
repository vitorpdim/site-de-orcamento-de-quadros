-- 1. CRIAÇÃO E USO DO BANCO DE DADOS
CREATE DATABASE IF NOT EXISTS orcamento_quadros_db CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci;
USE orcamento_quadros_db;

-- 2. APAGA AS TABELAS ANTIGAS PARA GARANTIR UMA INSTALAÇÃO LIMPA
SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE IF EXISTS Quadro_Materiais;
DROP TABLE IF EXISTS Quadro_Molduras;
DROP TABLE IF EXISTS Quadros;
DROP TABLE IF EXISTS Pedidos;
DROP TABLE IF EXISTS Clientes;
DROP TABLE IF EXISTS Molduras;
DROP TABLE IF EXISTS Materiais;
SET FOREIGN_KEY_CHECKS = 1;

-- 3. CRIAÇÃO DAS TABELAS (ESTRUTURA)
CREATE TABLE Molduras (id INT AUTO_INCREMENT PRIMARY KEY, codigo VARCHAR(50) NOT NULL UNIQUE, nome VARCHAR(100) NOT NULL, valor_metro_linear DECIMAL(10, 2) NOT NULL) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
CREATE TABLE Materiais (id INT AUTO_INCREMENT PRIMARY KEY, nome VARCHAR(100) NOT NULL UNIQUE, tipo_calculo ENUM('metro_linear', 'metro_quadrado') NOT NULL, valor_base DECIMAL(10, 2) NOT NULL) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
CREATE TABLE Clientes (id INT AUTO_INCREMENT PRIMARY KEY, nome VARCHAR(255) NOT NULL, telefone VARCHAR(50)) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
CREATE TABLE Pedidos (id INT AUTO_INCREMENT PRIMARY KEY, numero_pedido VARCHAR(10) NOT NULL UNIQUE, cliente_id INT, atendente VARCHAR(255) NOT NULL, data_criacao DATETIME DEFAULT CURRENT_TIMESTAMP, status ENUM('A Fazer', 'Já Feito', 'Entregue') DEFAULT 'A Fazer', observacoes TEXT, valor_final DECIMAL(10,2), pdf_blob LONGBLOB, pdf_filename VARCHAR(255), pdf_os_blob LONGBLOB, pdf_os_filename VARCHAR(255), FOREIGN KEY (cliente_id) REFERENCES Clientes(id) ON DELETE SET NULL) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
CREATE TABLE Quadros (id INT AUTO_INCREMENT PRIMARY KEY, pedido_id INT NOT NULL, altura_cm DECIMAL(10, 2) NOT NULL, largura_cm DECIMAL(10, 2) NOT NULL, medida_fornecida_cliente BOOLEAN DEFAULT FALSE, limpeza_flag BOOLEAN DEFAULT FALSE, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP, FOREIGN KEY (pedido_id) REFERENCES Pedidos(id) ON DELETE CASCADE) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
CREATE TABLE Quadro_Molduras (id INT AUTO_INCREMENT PRIMARY KEY, quadro_id INT NOT NULL, moldura_id INT NULL, valor_calculado_moldura DECIMAL(10, 2), FOREIGN KEY (quadro_id) REFERENCES Quadros(id) ON DELETE CASCADE, FOREIGN KEY (moldura_id) REFERENCES Molduras(id) ON DELETE SET NULL) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
CREATE TABLE Quadro_Materiais (id INT AUTO_INCREMENT PRIMARY KEY, quadro_id INT NOT NULL, material_id INT NULL, espessura_paspatur_cm DECIMAL(10, 2), valor_calculado_material DECIMAL(10, 2), FOREIGN KEY (quadro_id) REFERENCES Quadros(id) ON DELETE CASCADE, FOREIGN KEY (material_id) REFERENCES Materiais(id) ON DELETE SET NULL) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

